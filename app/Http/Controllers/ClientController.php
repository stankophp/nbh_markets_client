    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'name' => 'required',
            'surname' => 'required',
            'email' => 'required|email',
            'open_trades' => 'numeric|min:0',
            'closed_trades' => 'numeric|min:0',
        ]);
        $client = Client::findOrFail($id);


        $client->name = $request->get('name');
        $client->surname = $request->get('surname');
        $client->email = $request->get('email');
        $client->balance = $request->get('balance');
        $client->open_trades = $request->get('open_trades');
        $client->closed_trades = $request->get('closed_trades');

        $client->save();


        return redirect(route('clients.show', [$client->id]));
    }

